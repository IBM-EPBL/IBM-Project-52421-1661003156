list=['0','1','3','4']
#insert an integer
list.insert(2,'2')
print(list)
#print the list
print(list)
#remove the list
list.remove('0')
print(list)
#append the list
list.append('5')
print(list)
#sort the list
list.sort
print(list)
#pop the list
print('the original list is: '+str(list))
print('the last element using pop() is: '+str(list.pop()))
#reverse the list
list.reverse()
print('reverse list:',list)
